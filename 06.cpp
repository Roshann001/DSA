#include <iostream>
using namespace std;

int main() {
    int n = 5;

    // upper part
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= i; j++) {
            cout << i;
            if (j < i)
                cout << " * ";
        }
        cout << endl;
    }

    // lower part
    for (int i = n - 1; i >= 1; i--) {
        for (int j = 1; j <= i; j++) {
            cout << i;
            if (j < i)
                cout << " * ";
        }
        cout << endl;
    }

    return 0;
}